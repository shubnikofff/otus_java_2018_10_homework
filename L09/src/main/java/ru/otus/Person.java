package ru.otus;

public class Person {
	private String name;
	private int age;
	private String address;
	private boolean isMarried;

	public Person(String name, int age, String address, boolean isMarried) {
		this.name = name;
		this.age = age;
		this.address = address;
		this.isMarried = isMarried;
	}
}
